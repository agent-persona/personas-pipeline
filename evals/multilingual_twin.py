_STOP_WORDS = {
    "that", "this", "with", "from", "into", "their", "have", "been",
    "will", "each", "than", "them", "they", "when", "which", "also",
    "all", "any", "for", "the", "and", "are", "not", "our", "its",
    # common English verbs that appear as first words in goal strings
    "automate", "integrate", "build", "save", "present", "streamline",
    "export", "set", "provision",
}


def extract_core_traits(persona: dict) -> list[str]:
    """Return union of top-5 vocabulary terms + top-3 goals content-keywords + name.

    Goal keywords are extracted as the first non-stop, non-trivial content word
    (len > 4) from each goal string.  This deliberately uses English terms;
    counting them in Spanish/Mandarin responses measures *English-term retention*
    (i.e. code-switching) rather than semantic coverage — which is the metric
    this experiment is designed to capture.
    """
    vocabulary: list[str] = persona.get("vocabulary", [])[:5]
    goals: list[str] = persona.get("goals", [])[:3]
    goal_keywords: list[str] = []
    for goal in goals:
        words = [w.strip(".,;:") for w in goal.split() if len(w) > 4]
        # Skip leading stop/generic words to get a more distinctive keyword
        for w in words:
            if w.lower() not in _STOP_WORDS:
                goal_keywords.append(w.lower())
                break
    name = persona.get("name", "")
    traits = list(vocabulary) + goal_keywords + [name]
    # Deduplicate while preserving order
    seen: set[str] = set()
    unique: list[str] = []
    for t in traits:
        if t.lower() not in seen:
            seen.add(t.lower())
            unique.append(t)
    return unique


def count_traits_in_response(traits: list[str], response: str) -> int:
    response_lower = response.lower()
    return sum(1 for t in traits if t.lower() in response_lower)


def run_experiment(persona_dir: Path) -> dict:
    results: dict = {"personas": {}, "summary": {}}

    for fname in ["persona_00.json", "persona_01.json"]:
        persona_path = persona_dir / fname
        with open(persona_path) as f:
            record = json.load(f)

        persona = record["persona"]
        pid = fname.replace(".json", "")
        traits = extract_core_traits(persona)
        total_traits = len(traits)
        system_prompt = build_persona_system_prompt(persona)

        persona_results: dict = {
            "name": persona["name"],
            "system_prompt_preview": system_prompt[:200] + "...",
            "core_traits": traits,
            "total_traits": total_traits,
            "responses": {},
            "trait_preservation_rates": {},
        }

        for lang in ("english", "spanish", "mandarin"):
            response_text = RESPONSES[pid][lang]
            found = count_traits_in_response(traits, response_text)
            rate = round(found / total_traits, 4) if total_traits else 0.0
            persona_results["responses"][lang] = {
                "question": QUESTIONS[lang],
                "response": response_text,
                "traits_found": found,
                "traits_total": total_traits,
                "trait_preservation_rate": rate,
            }
            persona_results["trait_preservation_rates"][lang] = rate

        results["personas"][pid] = persona_results

    # Compute summary averages
    langs = ("english", "spanish", "mandarin")
    for lang in langs:
        rates = [
            results["personas"][pid]["trait_preservation_rates"][lang]
            for pid in results["personas"]
        ]
        results["summary"][lang] = round(sum(rates) / len(rates), 4)

    non_english_avg = round(
        (results["summary"]["spanish"] + results["summary"]["mandarin"]) / 2, 4
    )
    results["summary"]["non_english_average"] = non_english_avg

    drop_from_english = round(results["summary"]["english"] - non_english_avg, 4)
    results["summary"]["drop_from_english_control"] = drop_from_english

    if drop_from_english > 0.20:
        signal = "STRONG"
    elif drop_from_english > 0.10:
        signal = "MODERATE"
    elif drop_from_english > 0.05:
        signal = "WEAK"
    else:
        signal = "NOISE"
    results["summary"]["signal"] = signal

    return results


def main() -> None:
    repo_root = Path(__file__).parent.parent
    persona_dir = repo_root / "output"

    results = run_experiment(persona_dir)

    out_dir = repo_root / "output" / "experiments" / "exp-4.19-cross-language-coherence"
    out_dir.mkdir(parents=True, exist_ok=True)

    comparison_path = out_dir / "comparison.json"
    with open(comparison_path, "w") as f:
        json.dump(results, f, indent=2, ensure_ascii=False)
    print(f"Wrote: {comparison_path}")

    # Print summary table
    print("\n=== Trait Preservation Rate ===")
    print(f"{'Persona':<30} {'English':>10} {'Spanish':>10} {'Mandarin':>10}")
    print("-" * 62)
    for pid, pdata in results["personas"].items():
        rates = pdata["trait_preservation_rates"]
        print(
            f"{pdata['name'][:29]:<30} {rates['english']:>10.4f} "
            f"{rates['spanish']:>10.4f} {rates['mandarin']:>10.4f}"
        )
    s = results["summary"]
    print("-" * 62)
    print(
        f"{'Average':<30} {s['english']:>10.4f} {s['spanish']:>10.4f} {s['mandarin']:>10.4f}"
    )
    print(f"\nNon-English average: {s['non_english_average']:.4f}")
    print(f"Drop from English control: {s['drop_from_english_control']:.4f}")
    print(f"Signal: {s['signal']}")


if __name__ == "__main__":
    main()
