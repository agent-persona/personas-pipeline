"""
Experiment 4.19: Cross-language coherence
Measures trait preservation rate when user messages are in Spanish/Mandarin
while the persona prompt remains in English.

Since Claude Code IS the LLM, responses are hardcoded (generated in-character
by the same model that runs this script). No API calls needed.
"""
from __future__ import annotations

