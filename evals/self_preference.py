"""Experiment 5.03: Self-preference bias detection.

Tests whether LLM judges show systematic bias toward outputs generated by
their own model class. Synthesizes personas with multiple models, then has
each model score ALL outputs. If the diagonal of the synthesizer×judge matrix
is consistently higher than the off-diagonal, self-preference bias exists.

Key concepts:
  - **Preference matrix**: rows = synthesizer model, columns = judge model,
    cells = mean score across dimensions and personas.
  - **Preference delta**: for each judge, diagonal_score - mean(off-diagonal).
    Positive = self-preference bias.
  - **Debiasing prior**: per-judge offset that can be subtracted from scores
    when the judge evaluates its own model's output to correct for bias.

Usage:
    matrix = build_preference_matrix(all_scores)
    deltas = compute_preference_deltas(matrix)
    prior  = compute_debiasing_prior(deltas)
"""

from __future__ import annotations

import json
import logging
import math
import statistics
from dataclasses import dataclass, field
from typing import Sequence

from anthropic import AsyncAnthropic

logger = logging.getLogger(__name__)

# ── Rubric (same as the shared judge rubric) ─────────────────────────

DIMENSIONS = (
    "grounded",
    "distinctive",
    "coherent",
    "actionable",
    "voice_fidelity",
)

JUDGE_RUBRIC_PROMPT = """\
You are an expert evaluator of synthetic marketing personas. Score the \
persona below on each dimension using a 0.0-1.0 scale.

## Scoring rubric

- **grounded** (0.0-1.0): Are claims traceable to source data? Does the \
persona cite specific record IDs and provide evidence entries for goals, \
pains, motivations, and objections? 1.0 = every claim is grounded with \
high-confidence evidence. 0.0 = entirely fabricated with no data linkage.

- **distinctive** (0.0-1.0): Does the persona feel like a real individual \
rather than a generic average? Are the vocabulary, quotes, and motivations \
specific enough to distinguish this persona from any other? 1.0 = vivid, \
unique, and memorable. 0.0 = interchangeable boilerplate.

- **coherent** (0.0-1.0): Is the persona internally consistent? Do the \
demographics, firmographics, goals, pains, vocabulary, and quotes all \
describe the same plausible person? 1.0 = perfectly consistent. 0.0 = \
contradictory or incoherent across fields.

- **actionable** (0.0-1.0): Are the goals, pains, and objections specific \
enough to inform real product and marketing decisions? 1.0 = a product \
team could immediately act on these insights. 0.0 = vague platitudes \
that apply to anyone.

- **voice_fidelity** (0.0-1.0): Do the sample quotes sound like one \
consistent speaker? Is the vocabulary list coherent with the persona's \
role, industry, and level? 1.0 = quotes and vocabulary are distinctive \
and internally consistent. 0.0 = generic or inconsistent voice.

## Output format

Respond with ONLY a JSON object (no markdown fences, no commentary):
{
  "grounded": <float>,
  "distinctive": <float>,
  "coherent": <float>,
  "actionable": <float>,
  "voice_fidelity": <float>,
  "overall": <float>,
  "rationale": "<1-2 sentence justification>"
}

The "overall" score should be the unweighted mean of the five dimension scores.

## Persona to evaluate

"""


# ── Data types ───────────────────────────────────────────────────────

@dataclass
class ScoreResult:
    """One judge's scores on one persona."""
    synthesizer_model: str
    judge_model: str
    overall: float
    dimensions: dict[str, float]
    rationale: str
    cost_usd: float = 0.0
    error: str | None = None


@dataclass
class PreferenceMatrix:
    """Synthesizer x Judge score matrix.

    matrix[synth_model][judge_model] = mean overall score across personas.
    per_dim[synth_model][judge_model][dim] = mean score for that dimension.
    """
    synthesizer_models: list[str]
    judge_models: list[str]
    matrix: dict[str, dict[str, float]]
    per_dim: dict[str, dict[str, dict[str, float]]]
    n_personas_per_synth: int


@dataclass
class PreferenceDelta:
    """Self-preference delta for one judge."""
    judge_model: str
    diagonal_score: float          # score when judging own model's output
    off_diagonal_mean: float       # mean score when judging other models' output
    delta: float                   # diagonal - off_diagonal (positive = bias)
    per_dim_delta: dict[str, float] = field(default_factory=dict)


@dataclass
class DebiasingPrior:
    """Correction offsets to remove self-preference bias."""
    judge_offsets: dict[str, float]  # judge_model -> offset to subtract
    overall_bias: float              # mean delta across all judges
    bias_detected: bool              # True if overall_bias > threshold
    per_dim_offsets: dict[str, dict[str, float]] = field(default_factory=dict)


# ── LLM scoring ─────────────────────────────────────────────────────

async def score_persona_with_judge(
    client: AsyncAnthropic,
    judge_model: str,
    persona_dict: dict,
    synthesizer_model: str,
) -> ScoreResult:
    """Score a single persona with a single judge model."""
    persona_json = json.dumps(persona_dict, indent=2)
    prompt = JUDGE_RUBRIC_PROMPT + persona_json

    try:
        response = await client.messages.create(
            model=judge_model,
            max_tokens=1024,
            messages=[{"role": "user", "content": prompt}],
        )

        text = response.content[0].text.strip()
        if text.startswith("```"):
            text = text.split("\n", 1)[1]
            if text.endswith("```"):
                text = text[: text.rfind("```")]
            text = text.strip()

        parsed = json.loads(text)

        in_tok = response.usage.input_tokens
        out_tok = response.usage.output_tokens
        if "opus" in judge_model:
            cost = (in_tok * 15 + out_tok * 75) / 1_000_000
        elif "haiku" in judge_model:
            cost = (in_tok * 1 + out_tok * 5) / 1_000_000
        else:
            cost = (in_tok * 3 + out_tok * 15) / 1_000_000

        dims = {d: float(parsed.get(d, 0.0)) for d in DIMENSIONS}
        return ScoreResult(
            synthesizer_model=synthesizer_model,
            judge_model=judge_model,
            overall=float(parsed.get("overall", statistics.mean(dims.values()))),
            dimensions=dims,
            rationale=parsed.get("rationale", ""),
            cost_usd=cost,
        )
    except Exception as e:
        logger.error("Judge %s failed: %s", judge_model, e)
        return ScoreResult(
            synthesizer_model=synthesizer_model,
            judge_model=judge_model,
            overall=float("nan"),
            dimensions={d: float("nan") for d in DIMENSIONS},
            rationale="",
            error=str(e),
        )


# ── Preference analysis ─────────────────────────────────────────────

def _model_key(model: str) -> str:
    """Short key for a model name (for matching synthesizer to judge)."""
    m = model.lower()
    if "opus" in m:
        return "opus"
    if "sonnet" in m:
        return "sonnet"
    if "haiku" in m:
        return "haiku"
    return m


def build_preference_matrix(
    all_scores: list[ScoreResult],
    synthesizer_models: list[str],
    judge_models: list[str],
) -> PreferenceMatrix:
    """Build the synthesizer x judge preference matrix.

    Averages overall scores across all personas for each (synth, judge) pair.
    """
    # Group scores by (synth, judge)
    grouped: dict[str, dict[str, list[float]]] = {
        s: {j: [] for j in judge_models} for s in synthesizer_models
    }
    grouped_dim: dict[str, dict[str, dict[str, list[float]]]] = {
        s: {j: {d: [] for d in DIMENSIONS} for j in judge_models}
        for s in synthesizer_models
    }

    for sr in all_scores:
        if sr.error is not None:
            continue
        if sr.synthesizer_model in grouped and sr.judge_model in grouped[sr.synthesizer_model]:
            grouped[sr.synthesizer_model][sr.judge_model].append(sr.overall)
            for d in DIMENSIONS:
                v = sr.dimensions.get(d, float("nan"))
                if not math.isnan(v):
                    grouped_dim[sr.synthesizer_model][sr.judge_model][d].append(v)

    matrix: dict[str, dict[str, float]] = {}
    per_dim: dict[str, dict[str, dict[str, float]]] = {}
    n_per_synth = 0

    for s in synthesizer_models:
        matrix[s] = {}
        per_dim[s] = {}
        for j in judge_models:
            scores = grouped[s][j]
            matrix[s][j] = statistics.mean(scores) if scores else float("nan")
            per_dim[s][j] = {}
            for d in DIMENSIONS:
                vals = grouped_dim[s][j][d]
                per_dim[s][j][d] = statistics.mean(vals) if vals else float("nan")
            if s == synthesizer_models[0]:
                n_per_synth = max(n_per_synth, len(scores))

    return PreferenceMatrix(
        synthesizer_models=synthesizer_models,
        judge_models=judge_models,
        matrix=matrix,
        per_dim=per_dim,
        n_personas_per_synth=n_per_synth,
    )


def compute_preference_deltas(
    matrix: PreferenceMatrix,
) -> list[PreferenceDelta]:
    """Compute self-preference delta for each judge.

    For each judge J, find the synthesizer S that shares its model family
    (the "diagonal" cell). Delta = diagonal_score - mean(off_diagonal_scores).
    """
    deltas = []

    for judge in matrix.judge_models:
        judge_key = _model_key(judge)

        # Find the diagonal synthesizer (same model family as judge)
        diagonal_synth = None
        for synth in matrix.synthesizer_models:
            if _model_key(synth) == judge_key:
                diagonal_synth = synth
                break

        if diagonal_synth is None:
            continue

        diag_score = matrix.matrix[diagonal_synth][judge]
        if math.isnan(diag_score):
            continue

        off_diag_scores = []
        for synth in matrix.synthesizer_models:
            if synth == diagonal_synth:
                continue
            s = matrix.matrix[synth][judge]
            if not math.isnan(s):
                off_diag_scores.append(s)

        if not off_diag_scores:
            continue

        off_diag_mean = statistics.mean(off_diag_scores)

        # Per-dimension deltas
        per_dim_delta = {}
        for d in DIMENSIONS:
            d_diag = matrix.per_dim[diagonal_synth][judge].get(d, float("nan"))
            d_off = [
                matrix.per_dim[s][judge].get(d, float("nan"))
                for s in matrix.synthesizer_models if s != diagonal_synth
            ]
            d_off = [v for v in d_off if not math.isnan(v)]
            if not math.isnan(d_diag) and d_off:
                per_dim_delta[d] = d_diag - statistics.mean(d_off)
            else:
                per_dim_delta[d] = float("nan")

        deltas.append(PreferenceDelta(
            judge_model=judge,
            diagonal_score=diag_score,
            off_diagonal_mean=off_diag_mean,
            delta=diag_score - off_diag_mean,
            per_dim_delta=per_dim_delta,
        ))

    return deltas


def compute_debiasing_prior(
    deltas: list[PreferenceDelta],
    bias_threshold: float = 0.03,
) -> DebiasingPrior:
    """Compute debiasing correction from observed preference deltas.

    Args:
        bias_threshold: minimum overall bias to flag as detected.

    Returns:
        DebiasingPrior with per-judge offsets and overall bias assessment.
    """
    if not deltas:
        return DebiasingPrior(
            judge_offsets={},
            overall_bias=0.0,
            bias_detected=False,
        )

    judge_offsets = {d.judge_model: d.delta for d in deltas}
    valid_deltas = [d.delta for d in deltas if not math.isnan(d.delta)]
    overall_bias = statistics.mean(valid_deltas) if valid_deltas else 0.0

    per_dim_offsets: dict[str, dict[str, float]] = {}
    for d in deltas:
        per_dim_offsets[d.judge_model] = d.per_dim_delta

    return DebiasingPrior(
        judge_offsets=judge_offsets,
        overall_bias=overall_bias,
        bias_detected=abs(overall_bias) > bias_threshold,
        per_dim_offsets=per_dim_offsets,
    )
